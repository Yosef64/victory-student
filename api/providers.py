            PERCENTAGE = 0.25
            BONUS_AMOUNT = 10
            WEEKLY_BONUS_THRESHOLD = 5
            
            total_money = BASE_AMOUNT * PERCENTAGE 
            if not TWeek % 5:
                total_money += (TWeek % WEEKLY_BONUS_THRESHOLD) * BONUS_AMOUNT
